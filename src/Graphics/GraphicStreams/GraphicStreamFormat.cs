namespace TheNewEngine.Graphics
{
    /// <summary>
    /// Format of a graphic stream item.
    /// </summary>
    public enum GraphicStreamFormat
    {
        /// <summary>
        /// Vector3, which contains X, Y and Z values.
        /// </summary>
        Vector3,

        /// <summary>
        /// Color4, which contains R, G, B and A values.
        /// </summary>
        Color4,

        /// <summary>
        /// Float value.
        /// </summary>
        Float
    }
}